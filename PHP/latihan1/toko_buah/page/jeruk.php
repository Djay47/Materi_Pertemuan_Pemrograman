                    <h2>Jeruk</h2>
                    
                    <img src="../img/jeruk.jpg">
                    
                    <p>
                        Jeruk (bahasa Inggris: <i>orange</i>) adalah buah dari berbagai spesies citrus dalam famili Rutaceae; terutama mengacu pada <i>Citrus</i> × <i>sinensisi</i>, yang juga disebut <b>jeruk manis</b>, untuk membedakannya dari <i>Citrus</i> × <i>aurantium</i> terkait, yang disebut jeruk pahit. Jeruk manis bereproduksi secara aseksual (apomiksis melalui <i>nucellar embryony</i>); varietas jeruk manis muncul melalui mutasi.
                    </p>
                    
                    <p>
                        Jeruk adalah hibrida antara jeruk bali (<i>Citrus maxima</i>) dan mandarin (<i>Citrus reticulata</i>). Genom kloroplas, dan karena itu garis ibu, adalah jeruk bali. Jeruk manis telah memiliki genom lengkap yang diurutkan.
                    </p>
                     
                    <p>
                        Jeruk berasal dari wilayah yang meliputi Tiongkok Selatan, India Timur Laut, dan Myanmar, dan penyebutan jeruk manis paling awal adalah dalam literatur Tiongkok pada 314 SM. Hingga 1987, pohon jeruk ditemukan sebagai pohon buah yang paling banyak dibudidayakan di dunia.Pohon jeruk banyak ditanam di iklim tropis dan subtropis untuk buahnya yang manis. Buah jeruk dapat dimakan segar, atau diolah untuk jus atau kulitnya yang harum.Hingga 2012, jeruk manis menyumbang sekitar 70% dari produksi citrus.
                    </p>
                    
