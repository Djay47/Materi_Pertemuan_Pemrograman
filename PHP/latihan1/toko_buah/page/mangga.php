                    <p>
                        <b>Mangga</b> atau <b>mempelam</b> adalah nama sejenis buah, demikian pula nama pohonnya. Mangga termasuk ke dalam genus <i>Mangifera</i>, yang terdiri dari 35-40 anggota dari famili Anacardiaceae.
                    </p>
                        
                    <p>
                        Nama "<i>mangga</i>" berasal dari bahasa Tamil, <i>mankay</i>, yang berarti <i>man</i> "pohon mangga" + <i>kay</i>  "buah". Kata ini dibawa ke Eropa oleh orang-orang Portugis dan diserap menjadi <i>manga</i> (bahasa Portugis), <i>mango</i> (bahasa Spanyol dan Inggris) dan lainnya.
                    </p>

                    <p>
                        Mangga berasal dari daerah di sekitar perbatasan India dengan Burma, dan mangga telah menyebar ke Asia Tenggara sekitar 1500 tahun yang silam. Buah ini dikenal pula dalam berbagai bahasa daerah, seperti <i>pelem</i> atau <i>poh</i> (Jw.), Poh (Bl.), dan Paok (Sas.)
                    </p>
                    
