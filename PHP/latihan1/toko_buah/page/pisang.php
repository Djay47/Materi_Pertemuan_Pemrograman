                    <h2>Pisang</h2>

                    <img src="../img/pisang.jpg">

                    <p>
                        <strong>Pisang</strong> adalah nama umum yang diberikan pada tumbuhan terna berukuran besar dengan daun memanjang dan besar yang tumbuh langsung dari bagian tangkai. Batang pisang bersifat lunak karena terbentuk dari lapisan pelepah yang lunak dan panjang. Batang yang agak keras berada di bagian permukaan tanah. Pisang memiliki daun bertangkai yang berpencar dan mudah robek dengan bagian batang yang meruncing. Ukuran daun pada tiap spesies pisang juga berbeda-beda. Tangkai pisang menghasilkan bunga dalam jumlah yang banyak. Bagian bunga pada pisang akan membentuk buah yang disebut sisir. Buah pisang berkelompok dalam satu bunga majemuk dengan ukuran yang makin ke bawah makin mengecil.
                    </p>
                    
                    <p>
                        Dalam taksonomi, pisang termasuk dalam genus Musa dan famili Musaceae. Beragam spesies pisang tersebar di kawasan Malesia. Spesies pisang yang paling banyak dibudidayakan di dunia adalah pisang hutan. Jenis pisang hutan dapat tumbuh di hutan, bukit maupun di dataran rendah. Selain itu, pisang juga dapat ditanam bersama dengan tanaman lain seperti jagung dan ketela pohon.
                    </p>
                    
                    <p>
                        Pisang dapat dipanen kapan saja, karena pertumbuhannya yang sesuai dengan segala jenis musim. Kematian pohon pisang hanya terjadi ketika berbuah hanya sekali semasa hidupnya. Buah pisang dapat langsung dimakan atau dimasak terlebih dahulu. Nutrisi di dalam pisang bermanfaat bagi kesehatan tubuh manusia dan dapat pula dibuat sebagai obat tradisional.
                    </p>

                    <p>
                        Di berbagai daerah dan mancanegara pisang memiliki nama-nama khas tersendiri, beberapa diantaranya: <i>gadang</i> atau <i>gedhang</i> (Jawa), <i>biyu</i>(Bali), <i>chawuk</i> atau <i>cau</i> (Sunda), <i>punti</i> (Lampung), <i>unti</i> (Bugis), <i>koyo</i> (Ternate), <i>kula</i> (Banda), <i>uri</i> (Ambon), <i>tema</i> (Seram) dan <i>ounche</i> (Madagaskar).
                    </p>

