                    <h2>Definisi</h2>
                    
                    <p>
                      <strong>Buah</strong> adalah organ pada tumbuhan berbunga yang merupakan perkembangan lanjutan dari bakal buah (ovarium). Buah biasanya membungkus dan melindungi biji. Aneka rupa dan bentuk buah tidak terlepas kaitannya dengan fungsi utama buah, yakni sebagai pemencar biji tumbuhan.
                    </p>

                    <p>
                      Pengertian buah dalam lingkup pertanian (<em>hortikultular</em>) atau pangan adalah lebih luas daripada pengertian buah diatas dan biaanya disebut sebagai <strong>buah-buahan</strong>. Buah dalam pengertian ini tidak terbatas yang terbentuk dari bakal buah, melainkan dapat berasal dari perkembangan organ yang lain. Karena itu, untuk membedakan, buah yang sesuai menurut pengertian botani biasa disebut <strong>buah sejati</strong>.
                    </p>
                    
                    <p>
                      Buah sering kali memiliki nilai ekonomi sebagai bahan pangan maupun bahan baku industri karena didalamnya disimpan berbagai macam produk metabolisme tumbuhan, mula dari karbohidat, protein, lemak, vitamin, mineral, alkaloid, hingga, terpena dan terpenoid. Ilmu yang mempelajari segala hal tentnang buah dinamakan <strong>pomologi</strong>.
                    </p>
